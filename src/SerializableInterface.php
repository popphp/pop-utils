interface SerializableInterface extends \JsonSerializable
{

    /**
     * Serialize the array object
     *
     * @return string
     */
    public function serialize(): string;

    /**
     * Unserialize a string
     *
     * @param  string $string
     * @return mixed
     */
    public function unserialize(string $string): mixed;

}
